## Glossary
## 
## - Confounding (混杂): 
## In statistics, a confounding variable (also confounding factor, a confound, 
## or confounder) is an extraneous variable in a statistical model that 
## correlates (directly or inversely) with both the dependent variable and the 
## independent variable. (Wikipedia)
## 
## - splines (花键, 仿样): 
## In mathematics, a spline is a numeric function that is piecewise-defined by 
## polynomial functions, and which possesses a sufficiently high degree of
## smoothness at the places where the polynomial pieces connect (which are known 
## as knots). (Wikipedia)


